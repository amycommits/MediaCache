
package com.kaydub.mediacache.web.beans.media.music;

/**
 *
 * @author Kevin W.
 */
public class SongBean {
    
    private Long id;
    
    private String name;
    
    private String artist;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getArtist() {
        return artist;
    }

    public void setArtist(String artist) {
        this.artist = artist;
    }
    
    

}
